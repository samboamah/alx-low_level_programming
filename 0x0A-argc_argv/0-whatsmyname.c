#include <stdio.h>

/**
 * main - program that prints its name, with by a new line
 * @argc: number of command line arguments.
 * @argv: argument that stores the strings in an array of char*
 * Return: 0
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
